export async function generateFlashcards(
  topic: string,
): Promise<AIResponse<FlashcardContent[]>> {
  try {
    if (!topic || !topic.trim()) {
      return { success: false, error: ERROR_MESSAGES.INVALID_INPUT };
    }

    const normalized = normalizeTopic(topic);

    // Cache check
    if (ENABLE_CACHE) {
      const cached = cachingService.get<FlashcardContent[]>(normalized, "flashcards");
      if (cached) {
        logger.debug("AIService", "Returning cached flashcards", { topic: normalized.slice(0, 50) });
        return { success: true, data: cached, cached: true };
      }
    }

    // Try AI providers
    if (providerManager.hasAnyProvider()) {
      try {
        const result = await createStructuredResponse<{ cards: FlashcardContent[] }>(
          "flashcards",
          `${AI_PROMPTS.BASE_SYSTEM}\n\n${AI_PROMPTS.FLASHCARDS}`,
          `Create effective revision flashcards for "${topic}".\n\nReturn ONLY valid JSON with a "cards" array. No additional text.`,
          normalized,
        );

        const cards = result.data.cards;

        if (cards && Array.isArray(cards) && cards.length > 0) {
          const validCards = cards.filter((c) => c.front && c.back);

          if (validCards.length >= 3) {
            cachingService.set(normalized, validCards, "flashcards");
            return {
              success: true,
              data: validCards,
              provider: result.provider,
              latencyMs: result.latencyMs,
            };
          }

          logger.warn("AIService", "Too few valid flashcards from AI", {
            total: cards.length,
            valid: validCards.length,
            provider: result.provider,
          });
        }
      } catch (aiError) {
        logger.warn("AIService", "AI flashcard generation failed, trying Wikipedia fallback", {
          error: (aiError as Error).message,
        });
      }
    }

    // Fallback: return error (Groq-only mode)
    return {
      success: false,
      error: "Flashcard generation is temporarily unavailable. Please try again shortly.",
    };
  } catch (error) {
    logger.error("AIService", "Flashcard generation completely failed", error as Error, { topic });
    return { success: false, error: ERROR_MESSAGES.AI_UNAVAILABLE };
  }
}

