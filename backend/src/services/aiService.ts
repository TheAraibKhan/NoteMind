export async function generateQuizQuestions(
  topic: string,
): Promise<AIResponse<QuizQuestion[]>> {
  try {
    if (!topic || !topic.trim()) {
      return { success: false, error: ERROR_MESSAGES.INVALID_INPUT };
    }

    const normalized = normalizeTopic(topic);

    // Cache check
    if (ENABLE_CACHE) {
      const cached = cachingService.get<QuizQuestion[]>(normalized, "quiz");
      if (cached) {
        logger.debug("AIService", "Returning cached quiz", { topic: normalized.slice(0, 50) });
        return { success: true, data: cached, cached: true };
      }
    }

    // Try AI providers
    if (providerManager.hasAnyProvider()) {
      try {
        const result = await createStructuredResponse<{ questions: QuizQuestion[] }>(
          "quiz_questions",
          `${AI_PROMPTS.BASE_SYSTEM}\n\n${AI_PROMPTS.QUIZ_QUESTIONS}`,
          `Create 5 multiple-choice quiz questions on "${topic}" that test genuine understanding.\n\nReturn ONLY valid JSON with a "questions" array. No additional text.`,
          normalized,
        );

        const questions = result.data.questions;

        if (questions && Array.isArray(questions) && questions.length > 0) {
          // Validate and fix each question
          const validQuestions = questions.filter((q) => {
            if (typeof q.correctAnswer !== "number" || q.correctAnswer < 0 || q.correctAnswer > 3) return false;
            if (!Array.isArray(q.options) || q.options.length !== 4) return false;
            if (!q.question || !q.explanation) return false;
            return true;
          });

          if (validQuestions.length >= 3) {
            cachingService.set(normalized, validQuestions, "quiz");
            return {
              success: true,
              data: validQuestions,
              provider: result.provider,
              latencyMs: result.latencyMs,
            };
          }

          logger.warn("AIService", "Too few valid quiz questions from AI", {
            total: questions.length,
            valid: validQuestions.length,
            provider: result.provider,
          });
        }
      } catch (aiError) {
        logger.warn("AIService", "AI quiz generation failed, trying Wikipedia fallback", {
          error: (aiError as Error).message,
        });
      }
    }

    // Fallback: return error (Groq-only mode)
    return {
      success: false,
      error: "Quiz generation is temporarily unavailable. Please try again shortly.",
    };
  } catch (error) {
    logger.error("AIService", "Quiz generation completely failed", error as Error, { topic });
    return { success: false, error: ERROR_MESSAGES.AI_UNAVAILABLE };
  }
}

