// ============================================================================

/**
 * Generate a structured response by sending a prompt through the provider chain.
 * Parses the response as JSON and returns the typed result.
 * Throws on failure (caller handles fallback).
 */
async function createStructuredResponse<T>(
  name: string,
  systemPrompt: string,
  userPrompt: string,
  topic: string,
): Promise<{ data: T; provider: string; latencyMs: number }> {
  const fullPrompt = `${systemPrompt}\n\n---\n\nUSER REQUEST:\n${userPrompt}`;

  const result = await providerManager.generate(fullPrompt, {
    temperature: 0.7,
    maxTokens: 4096,
  });

  if (!result) {
    throw new Error("ALL_PROVIDERS_FAILED");
  }

  logger.info("AIService", `${name} generated by ${result.provider}`, {
    topic: topic.slice(0, 50),
    responseLength: result.text.length,
    latencyMs: result.latencyMs,
    provider: result.provider,
  });

  // Parse JSON
  let parsed: T;
  try {
    const jsonString = extractJSON(result.text);
    parsed = JSON.parse(jsonString) as T;
  } catch (parseError) {
    logger.error("AIService", `Failed to parse ${name} response as JSON`, parseError as Error, {
      rawResponse: result.text.slice(0, 300),
      provider: result.provider,
    });
    throw new Error("Invalid JSON response from AI");
  }

  return { data: parsed, provider: result.provider, latencyMs: result.latencyMs };
}

/**
 * Generate a free-text response (not JSON) through the provider chain.
 */
async function createTextResponse(
  name: string,
  prompt: string,
  topic: string,
): Promise<{ text: string; provider: string; latencyMs: number }> {
  const result = await providerManager.generate(prompt, {
    temperature: 0.7,
    maxTokens: 2000,
  });

  if (!result) {
    throw new Error("ALL_PROVIDERS_FAILED");
  }

  logger.info("AIService", `${name} answered by ${result.provider}`, {
    topic: topic.slice(0, 50),
    responseLength: result.text.length,
    latencyMs: result.latencyMs,
    provider: result.provider,
  });

  return { text: result.text, provider: result.provider, latencyMs: result.latencyMs };
}

